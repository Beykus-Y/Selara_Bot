def _docs_item(
    title: str,
    *,
    text: str,
    badges: tuple[str, ...] = (),
    commands: tuple[str, ...] = (),
    triggers: tuple[str, ...] = (),
    examples: tuple[str, ...] = (),
    steps: tuple[str, ...] = (),
    notes: tuple[str, ...] = (),
) -> dict[str, Any]:
    return {
        "title": title,
        "text": text,
        "badges": badges,
        "commands": commands,
        "triggers": triggers,
        "examples": examples,
        "steps": steps,
        "notes": notes,
    }


def _docs_section(
    anchor: str,
    title: str,
    summary: str,
    *items: dict[str, Any],
) -> dict[str, Any]:
    return {
        "anchor": anchor,
        "title": title,
        "summary": summary,
        "items": items,
    }


