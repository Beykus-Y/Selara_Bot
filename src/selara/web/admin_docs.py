def setting_anchor(key: str) -> str:
    return f"setting-{key}"


def trigger_match_type_label_ru(value: str) -> str:
    return TRIGGER_MATCH_TYPE_LABELS_RU.get(value, value)


def build_trigger_match_types() -> list[dict[str, str]]:
    return [
        {
            "code": code,
            "label": trigger_match_type_label_ru(code),
            "description": TRIGGER_MATCH_TYPE_HELP_RU[code],
        }
        for code in ("exact", "contains", "starts_with")
    ]


def build_settings_docs_sections() -> list[dict[str, Any]]:
    grouped_keys: set[str] = set()
    sections: list[dict[str, Any]] = []

    for index, (group_title, keys) in enumerate(SETTINGS_GROUPS, start=1):
        items: list[dict[str, str]] = []
        for key in keys:
            if key not in CHAT_SETTINGS_KEYS:
                continue
            grouped_keys.add(key)
            items.append(
                {
                    "anchor": setting_anchor(key),
                    "key": key,
                    "title": setting_title_ru(key),
                    "description": setting_description_ru(key),
                    "value_hint": setting_value_hint_ru(key),
                }
            )
        sections.append(
            {
                "anchor": f"settings-group-{index}",
                "title": group_title,
                "items": items,
            }
        )

    remaining = [key for key in CHAT_SETTINGS_KEYS if key not in grouped_keys]
    if remaining:
        sections.append(
            {
                "anchor": "settings-group-other",
                "title": "Прочее",
                "items": [
                    {
                        "anchor": setting_anchor(key),
                        "key": key,
                        "title": setting_title_ru(key),
                        "description": setting_description_ru(key),
                        "value_hint": setting_value_hint_ru(key),
                    }
                    for key in remaining
                ],
            }
        )

    return sections


def build_admin_docs_context(*, chat: UserChatOverview | None) -> dict[str, Any]:
    settings_sections = build_settings_docs_sections()
    docs_sections = [dict(section) for section in _FEATURE_DOC_SECTIONS]
    return {
        "page_title": "Selara • Документация администратора",
        "page_name": "admin-docs",
        "hero_title": "Документация администратора",
        "hero_subtitle": (
            "Полная справка по веб-панели: от входа и ролей до настроек, алиасов, смарт-триггеров, аудита и игровых разделов."
        ),
        "docs_sections": docs_sections,
        "trigger_match_types": build_trigger_match_types(),
        "trigger_template_variable_groups": build_trigger_template_variable_groups(),
        "settings_docs_sections": settings_sections,
        "origin_chat": (
            {
                "href": f"/app/chat/{chat.chat_id}",
                "label": chat.chat_title or f"chat:{chat.chat_id}",
            }
            if chat is not None
            else {
                "href": "/app",
                "label": "кабинет",
            }
        ),
    }
